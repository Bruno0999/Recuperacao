    public BcmProdutos viewBean(){
      //int\/
        BcmProdutos bcmProdutos = new BcmProdutos();
        int id = Integer.valueOf(JTxtID.getText());
        bcmProdutos.setIdbcmProdutos(id);
        
         //string\/
        bcmProdutos.setBcmNome(JTxtNome.getText());
        bcmProdutos.setBcmDescricao(jTxtDescricao.getText());
        bcmProdutos.setBcmPreco(jTxtPreco.getText());
         
         //iindex\/
        bcmProdutos.setBcmCategoria(jCboSabor.getSelectedIndex());       
         //data\/
         SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        jFmtData.getText();
        try {
          bcmProdutos.setBcmDataRegistro(formato.parse(jFmtData.getText()));
           } catch (ParseException ex) {
        System.out.println("Erro:" + ex.getMessage());
            }
        
        return bcmProdutos;
    }
    public BcmProdutos beanviaw(BcmProdutos bcmProdutos){
         //int\/
        JTxtID.setText( String.valueOf(bcmProdutos.getIdbcmProdutos() ) );
        //string\/
       JTxtNome.setText(bcmProdutos.getBcmNome());
       jTxtDescricao.setText(bcmProdutos.getBcmDescricao());
          //index\/
       jCboSabor.setSelectedIndex(bcmProdutos.getBcmCategoria());
        //data\/
        
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        
        jFmtData.setText(formato.format(bcmProdutos.getBcmDataRegistro()));
        return bcmProdutos;
      
     
    }

